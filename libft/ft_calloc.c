#include <stdlib.h>

static void	*ft_memmset(void *s, int c, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = s;
	while (i < n)
	{
		str[i] = c;
		i++;
	}
	s = str;
	return (s);
}

void		*ft_calloc(size_t nmemb, size_t size)
{
	void	*mallo;

	if (!(mallo = (void *)malloc(nmemb * size)))
		return (NULL);
	return (ft_memmset(mallo, 0, nmemb * size));
}
